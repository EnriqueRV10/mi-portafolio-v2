// components/react/ProjectLinks.tsx
import React from "react";
import type { ProjectLink } from "./types";

interface ProjectLinksProps {
  links?: ProjectLink[];
  hasDetails?: boolean;
  onViewDetails?: () => void;
  className?: string;
}

const ProjectLinks: React.FC<ProjectLinksProps> = ({
  links = [],
  hasDetails = false,
  onViewDetails,
  className = "",
}) => {
  const getIcon = (type: ProjectLink["type"]) => {
    switch (type) {
      case "github":
        return (
          <svg className="w-4 h-4" fill="currentColor" viewBox="0 0 24 24">
            <path d="M12 0c-6.626 0-12 5.373-12 12 0 5.302 3.438 9.8 8.207 11.387.599.111.793-.261.793-.577v-2.234c-3.338.726-4.033-1.416-4.033-1.416-.546-1.387-1.333-1.756-1.333-1.756-1.089-.745.083-.729.083-.729 1.205.084 1.839 1.237 1.839 1.237 1.07 1.834 2.807 1.304 3.492.997.107-.775.418-1.305.762-1.604-2.665-.305-5.467-1.334-5.467-5.931 0-1.311.469-2.381 1.236-3.221-.124-.303-.535-1.524.117-3.176 0 0 1.008-.322 3.301 1.23.957-.266 1.983-.399 3.003-.404 1.02.005 2.047.138 3.006.404 2.291-1.552 3.297-1.23 3.297-1.23.653 1.653.242 2.874.118 3.176.77.84 1.235 1.911 1.235 3.221 0 4.609-2.807 5.624-5.479 5.921.43.372.823 1.102.823 2.222v3.293c0 .319.192.694.801.576 4.765-1.589 8.199-6.086 8.199-11.386 0-6.627-5.373-12-12-12z" />
          </svg>
        );
      case "live":
        return (
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14"
            />
          </svg>
        );
      case "demo":
        return (
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M15 10l4.553-2.276A1 1 0 0121 8.618v6.764a1 1 0 01-1.447.894L15 14M5 18h8a2 2 0 002-2V8a2 2 0 00-2-2H5a2 2 0 00-2 2v8a2 2 0 002 2z"
            />
          </svg>
        );
      case "download":
        return (
          <svg
            className="w-4 h-4"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M12 10v6m0 0l-3-3m3 3l3-3m2 8H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z"
            />
          </svg>
        );
      default:
        return null;
    }
  };

  const getLinkColor = (type: ProjectLink["type"]) => {
    switch (type) {
      case "github":
        return "text-neutral-400 hover:text-white";
      case "live":
        return "text-blue-400 hover:text-blue-300";
      case "demo":
        return "text-emerald-400 hover:text-emerald-300";
      case "download":
        return "text-purple-400 hover:text-purple-300";
      default:
        return "text-neutral-400 hover:text-white";
    }
  };

  return (
    <div className={`flex items-center justify-between ${className}`}>
      {/* Enlaces externos */}
      <div className="flex gap-3">
        {links.map((link, index) => (
          <a
            key={index}
            href={link.url}
            target="_blank"
            rel="noopener noreferrer"
            className={`flex items-center gap-1.5 text-xs font-medium transition-colors ${getLinkColor(link.type)} hover:scale-105 transform duration-200`}
            title={link.label || link.type}
          >
            {getIcon(link.type)}
            <span className="hidden sm:inline">{link.label || link.type}</span>
          </a>
        ))}
      </div>

      {/* Bot√≥n de detalles */}
      {hasDetails && onViewDetails && (
        <button
          onClick={onViewDetails}
          className="text-emerald-500 hover:text-emerald-400 transition-colors group-hover:translate-x-1 transform duration-200 flex items-center gap-1 text-xs font-medium"
        >
          <span>Ver detalles</span>
          <svg
            className="w-3 h-3"
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M9 5l7 7-7 7"
            />
          </svg>
        </button>
      )}
    </div>
  );
};

export default ProjectLinks;
